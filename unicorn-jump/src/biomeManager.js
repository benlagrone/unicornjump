export const getBiomeConfig = (index) =>
  BIOMES[Math.min(BIOMES.length - 1, Math.max(0, index))];

export const getNextBiomeConfig = (index) =>
  index + 1 < BIOMES.length ? BIOMES[index + 1] : null;

const expandLayer = (entries, viewport, levelHeight, layerId) =>
  entries.map((entry, index) => ({
    ...entry,
    id: `${layerId}-${index}`,
    left: entry.x * viewport.width,
    top: entry.y * levelHeight,
    pixelWidth: entry.width * viewport.width,
    pixelHeight: entry.height * viewport.height,
  }));

export const buildBiomeDecorations = (biome, viewport, levelHeight) => ({
  far: expandLayer(biome.decorations.far, viewport, levelHeight, `${biome.id}-far`),
  mid: expandLayer(biome.decorations.mid, viewport, levelHeight, `${biome.id}-mid`),
  fore: expandLayer(biome.decorations.fore, viewport, levelHeight, `${biome.id}-fore`),
});
